export type AppMessageKey = keyof typeof appMessages.ja;

export function translateApp(localeValue: unknown, key: AppMessageKey, values: AppMessageValues = {}) {
  const locale = normalizeAppLocale(localeValue);
  const messages = appMessages[locale] as Record<AppMessageKey, string>;
  return Object.entries(values).reduce(
    (message, [name, value]) => message.replaceAll(`{${name}}`, String(value)),
    messages[key] ?? appMessages.ja[key],
  );
}

export function appIntlLocale(localeValue: unknown) {
  return normalizeAppLocale(localeValue) === "en" ? "en-US" : "ja-JP";
}

export function appLocaleHtmlLang(localeValue: unknown): AppLocale {
  return normalizeAppLocale(localeValue);
}
